package org.siegan.dojo.model;

public class Point {
    private int x;
    private int y;

    public Point(int y, int x){
        this.x = x;
        this.y = y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
